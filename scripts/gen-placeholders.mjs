// Generates tiny, blurred Low-Quality Image Placeholders (LQIP) for every
// JPEG in /img and writes them as inline base64 data URIs to
// src/data/imagePlaceholders.js.
//
// The placeholders are ~24px on their longest edge, so each one is well under
// 1KB and ships inside the JS bundle — meaning it paints instantly with zero
// network requests, even on slow connections. The full-resolution image is
// layered on top and covers the placeholder once it finishes downloading.
//
// Regenerate after adding/replacing images:  node scripts/gen-placeholders.mjs

import { execFileSync } from 'node:child_process'
import { readdirSync, readFileSync, writeFileSync, mkdtempSync, rmSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { join, dirname, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const IMG_DIR = join(__dirname, '..', 'img')
const OUT_FILE = join(__dirname, '..', 'src', 'data', 'imagePlaceholders.js')
const MAX_EDGE = 24 // longest-edge pixels for the placeholder

const tmp = mkdtempSync(join(tmpdir(), 'lqip-'))
try {
  const files = readdirSync(IMG_DIR).filter((f) => /\.jpe?g$/i.test(f)).sort()
  const entries = files.map((file) => {
    const src = join(IMG_DIR, file)
    const out = join(tmp, file)
    // Downscale to a tiny thumbnail at reduced quality. The upscaling done by
    // background-size:cover (plus the existing intro blur) hides the artifacts.
    execFileSync('sips', [
      '-Z', String(MAX_EDGE),
      '-s', 'formatOptions', '40',
      src, '--out', out,
    ], { stdio: 'ignore' })
    const b64 = readFileSync(out).toString('base64')
    return [basename(file), `data:image/jpeg;base64,${b64}`]
  })

  const body =
    '// AUTO-GENERATED by scripts/gen-placeholders.mjs — do not edit by hand.\n' +
    '// Tiny inline LQIP data URIs keyed by image filename. See lqip() in this module.\n\n' +
    'const placeholders = {\n' +
    entries.map(([name, uri]) => `  ${JSON.stringify(name)}: ${JSON.stringify(uri)},`).join('\n') +
    '\n}\n\n' +
    '// Build a layered `background-image` value: the full image on top, the inline\n' +
    '// placeholder underneath. The placeholder paints instantly; the full image\n' +
    '// covers it once loaded. Pass a Vite-resolved image URL (e.g. an import).\n' +
    'export function lqip(fullUrl) {\n' +
    '  const key = fullUrl.split("/").pop().split("?")[0]\n' +
    '  const ph = placeholders[key]\n' +
    '  return ph ? `url(${fullUrl}), url(${ph})` : `url(${fullUrl})`\n' +
    '}\n\n' +
    'export default placeholders\n'

  writeFileSync(OUT_FILE, body)
  console.log(`Wrote ${entries.length} placeholders to ${OUT_FILE}`)
} finally {
  rmSync(tmp, { recursive: true, force: true })
}
